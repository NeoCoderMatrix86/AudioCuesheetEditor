using Microsoft.JSInterop;

namespace AudioCuesheetEditor.Services.UI
{
    public class BoundingClientRect
    {
        public double X { get; set; }
        public double Y { get; set; }
        public double Width { get; set; }
        public double Height { get; set; }
        public double Top { get; set; }
        public double Right { get; set; }
        public double Bottom { get; set; }
        public double Left { get; set; }
    }

    public class BaseComponentManager(IJSRuntime jsRuntime)
    {
        private readonly IJSRuntime _jsRuntime = jsRuntime;

        public async Task<BoundingClientRect> GetBoundingClientRectAsync(string elementName)
        {
            return await _jsRuntime.InvokeAsync<BoundingClientRect>("getBoundingClientRect", elementName);
        }
    }
}
